exports.main = async (event, context) => {
  const res = {
    account: null,
    balance: 0,
    token: [],
    debug: null,
  };
  const { openId } = event.userInfo;
  const dbRes = await db.collection('wallet').where({ openId }).get();
  const dbData = dbRes.data[0];
  const { account, contract } = dbData;
  res.account = account;
  const balance = await web3.eth.getBalance(account);
  res.balance = balance;
  if (contract.length > 0) {
    contract.forEach((currentContract) => {
      const tokenItem = {};
      const MyContract = new web3.eth.Contract(contractAbi, currentContract, {
        from: account,
      });
      MyContract.methods['0x06fdde03']()
        .call({ from: currentContract })
        .then((res) => (res.debug = res));
      res.token.push(tokenItem);
    });
  }
  return res;
};
